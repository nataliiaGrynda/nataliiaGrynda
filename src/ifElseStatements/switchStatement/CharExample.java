package ifElseStatements.switchStatement;

public class CharExample {
  public static void main(String[] args){


    char character = '$';

    switch (character){
      case'A':
      System.out.println("it is uper case and is the first letter it the alphabet");
      break;
      case'5':
        System.out.println("it is a digit");
        break;
      case'$':
        System.out.println("it is dollar sign");
      break;
      default:
        System.out.println("it not a, 5 or $");
break;






    }






  }
}
